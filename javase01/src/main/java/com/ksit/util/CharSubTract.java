package com.ksit.util;

class CharSubTract{

	public static void main(String args[]){

		char char1 = 'a';
		char char2 = 'b';
		
		//将两个char类型做加法,结果会用两个char类型对应的asc码来做加法
		System.out.println("char1 + char2 :" + (char1 + char2));
		//将两个char类型的asc码做减法
		System.out.println("char1 - char2 :" + (char1 - char2));


	}
}
