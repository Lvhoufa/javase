package com.ksit.util;

class LongAndFloat {
	
	public static void main(String args[]){
		
		long num1 = 1L;
		float num2 = 1.5F;
		
		System.out.println("num1 value(long): " + num1);
		System.out.println("num2 value(float): " + num2);
		
		byte num3 = 127;
		System.out.println("num3 value(byte): " + num3);
		
		
		
	}
}